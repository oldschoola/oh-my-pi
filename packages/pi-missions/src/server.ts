export interface StartServerOptions {
	port?: number;
	cwd?: string;
}

export async function startServer(options: StartServerOptions = {}): Promise<{ port: number; stop: () => void }> {
	await ensureClientBuild();
	const cwd = options.cwd ?? process.cwd();
	const port = options.port ?? 3848;

	const server = Bun.serve({
		port,
		async fetch(req) {
			const url = new URL(req.url);
			const p = url.pathname;

			const corsHeaders = {
				"Access-Control-Allow-Origin": "*",
				"Access-Control-Allow-Methods": "GET, POST, OPTIONS",
				"Access-Control-Allow-Headers": "Content-Type",
			};

			if (req.method === "OPTIONS") {
				return new Response(null, { headers: corsHeaders });
			}

			try {
				const response = p.startsWith("/api/") ? await handleApi(req, cwd) : await handleStatic(p);

				const headers = new Headers(response.headers);
				for (const [k, v] of Object.entries(corsHeaders)) headers.set(k, v);
				return new Response(response.body, { status: response.status, headers });
			} catch (error) {
				console.error("MissionControl server error:", error);
				return jsonResponse(
					{ error: error instanceof Error ? error.message : "Unknown error" },
					{ status: 500, headers: corsHeaders },
				);
			}
		},
	});

	return {
		port: server.port ?? port,
		stop: () => server.stop(),
	};
}
