function jsonResponse(data: unknown, init: ResponseInit = {}): Response {
	return new Response(JSON.stringify(data), {
		...init,
		headers: { "Content-Type": "application/json", ...(init.headers ?? {}) },
	});
}

async function handleApi(req: Request, cwd: string): Promise<Response> {
	const url = new URL(req.url);
	const p = url.pathname;

	if (p === "/api/missions" && req.method === "GET") {
		const missions = await listMissions(cwd);
		return jsonResponse(missions);
	}

	const detailMatch = p.match(/^\/api\/mission\/([^/]+)$/);
	if (detailMatch && req.method === "GET") {
		const detail = await getMission(cwd, detailMatch[1]);
		if (!detail) return new Response("Not Found", { status: 404 });
		return jsonResponse(detail);
	}

	const eventsMatch = p.match(/^\/api\/mission\/([^/]+)\/events$/);
	if (eventsMatch && req.method === "GET") {
		const role = url.searchParams.get("role") ?? "worker";
		const events = await getMissionEvents(cwd, eventsMatch[1], role);
		return jsonResponse({ events });
	}

	const streamMatch = p.match(/^\/api\/mission\/([^/]+)\/stream$/);
	if (streamMatch && req.method === "GET") {
		return buildSSEStream(cwd, streamMatch[1]);
	}

	const telemetryMatch = p.match(/^\/api\/mission\/([^/]+)\/telemetry$/);
	if (telemetryMatch && req.method === "GET") {
		const summary = await getMissionTelemetrySummary(cwd, telemetryMatch[1]);
		return jsonResponse(summary ?? {});
	}

	const controlMatch = p.match(/^\/api\/mission\/([^/]+)\/(pause|resume|abort)$/);
	if (controlMatch && req.method === "POST") {
		const [, id, action] = controlMatch;
		const state = await loadActiveBatch(cwd);
		if (!state?.batch || (state.batch.batchId !== id && id !== "active")) {
			return new Response("No active batch mission", { status: 404 });
		}
		let next = state;
		if (action === "pause") next = pauseBatch(state);
		else if (action === "resume") next = resumeBatch(state);
		else if (action === "abort") {
			const body = (await req.json().catch(() => ({}))) as { reason?: string };
			next = abortBatch(state, body.reason ?? "aborted via dashboard");
		}
		await saveActiveBatch(cwd, next);
		return jsonResponse({ ok: true, phase: next.batch?.phase });
	}

	return new Response("Not Found", { status: 404 });
}

function buildSSEStream(cwd: string, missionId: string): Response {
	const encoder = new TextEncoder();
	const stream = new ReadableStream({
		async start(controller) {
			let cancelled = false;

			const send = (payload: unknown): void => {
				if (cancelled) return;
				controller.enqueue(encoder.encode(`data: ${JSON.stringify(payload)}\n\n`));
			};

			const tick = async (): Promise<void> => {
				const detail = await getMission(cwd, missionId);
				send({ type: "snapshot", detail });
			};

			await tick();
			const interval = setInterval(() => {
				if (cancelled) return;
				void tick();
			}, 2000);

			(controller as ReadableStreamDefaultController & { signal?: AbortSignal }).signal?.addEventListener?.(
				"abort",
				() => {
					cancelled = true;
					clearInterval(interval);
				},
			);
		},
	});
	return new Response(stream, {
		headers: {
			"Content-Type": "text/event-stream",
			"Cache-Control": "no-cache",
			Connection: "keep-alive",
		},
	});
}

async function handleStatic(requestPath: string): Promise<Response> {
	const staticDir = IS_BUN_COMPILED ? await getCompiledClientDir() : STATIC_DIR;
	const filePath = requestPath === "/" ? "/index.html" : requestPath;
	const fullPath = path.join(staticDir, filePath);

	const file = Bun.file(fullPath);
	if (await file.exists()) return new Response(file);

	const index = Bun.file(path.join(staticDir, "index.html"));
	if (await index.exists()) return new Response(index);

	return placeholderPage();
}

function placeholderPage(): Response {
	const body = `<!DOCTYPE html>
<html>
<head><meta charset="utf-8"><title>MissionControl</title></head>
<body style="font-family: system-ui; padding: 40px; max-width: 640px; margin: auto;">
  <h1>MissionControl</h1>
  <p>Dashboard is running but the client bundle has not been built yet.</p>
  <p>Run <code>bun run build</code> inside <code>packages/pi-missions</code> to generate the UI.</p>
  <h2>API</h2>
  <ul>
    <li><a href="/api/missions">/api/missions</a></li>
  </ul>
</body>
</html>`;
	return new Response(body, { headers: { "Content-Type": "text/html" } });
}

